package com.wiley.scalacohort.training

object CharacterLiterals extends App {

  //backspace
  println("test \btest")
  //horizontal tab
  println("\ttest")
  //line feed
  println("\ntest")
  //form feed
  println("\ftest")
  //carriage return
  println("\rtest")
  //double quote
  println("\"test\"")
  //single quote
  println("\'test\'")
  //backslash
  println("\\test")
  //printing an error
  System.err.println("test")

}
